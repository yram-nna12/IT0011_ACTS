# Asking for user input
Fname = input("Enter Your First Name: ")
Lname = input("Enter Your Last Name: ")

# Displaying the input details
# String Concatenation 
FulName = Fname + " " + Lname
print("Full Name: ", FulName)

# String: Upper() Method 
print("Full Name (Upper Case):", FulName.upper())

# String: Lower() Method 
print("Full Name (Lower Case):", FulName.lower())

# Len Method 
print("Length of Full Name:", len(FulName))
